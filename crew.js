function sortTasks() {
    var tasks = Array.from(document.querySelectorAll(".task"));

    var specialClickStatus = 0; // Hier den gewünschten data-click-status-Wert angeben

    tasks.sort(function (a, b) {
        var clickStatusA = parseInt(a.getAttribute("data-click-status")) || 0;
        var clickStatusB = parseInt(b.getAttribute("data-click-status")) || 0;

        if (clickStatusA === specialClickStatus && clickStatusB !== specialClickStatus) {
            return 1;
        } else if (clickStatusA !== specialClickStatus && clickStatusB === specialClickStatus) {
            return -1;
        }

        return clickStatusA - clickStatusB;
    });

    var container = document.querySelector(".container");
    container.innerHTML = '';

    tasks.forEach(function (task) {
        container.appendChild(task);
    });
}
