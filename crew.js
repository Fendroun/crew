function changePlayerColor(event) {
    // Setze die gewünschten Hintergrundfarben hier
    switch(playerInt) {
        case 3:
            var colors = [colorPlayer1, colorPlayer2,  colorPlayer3, colorTaskStandard];
            break
        case 4:
            var colors = [colorPlayer1, colorPlayer2,  colorPlayer3, colorPlayer4, colorTaskStandard];
            break 
        case 5:  
            var colors = [colorPlayer1, colorPlayer2,  colorPlayer3, colorPlayer4, colorPlayer5, colorTaskStandard];
            break;
    }
    // initial entspricht der ursprünglichen Farbe

    // Abrufen des aktuellen Klick-Status aus dem Datenattribut
    var clickStatus = parseInt(event.currentTarget.getAttribute("data-click-status")) || 0;

    // Ändere die Hintergrundfarbe basierend auf dem Klick-Status
    event.currentTarget.style.backgroundColor = colors[clickStatus % colors.length];

    // Inkrementiere den Klick-Status und speichere ihn im Datenattribut
    event.currentTarget.setAttribute("data-click-status", (clickStatus + 1) % colors.length);
}

